package com.rahul.dsa.basics;

public class DataTypes {
    static void main() {


        long value1 = 123456789;
        int value2 = (int)value1;
        System.out.println(value2);


        // primitive data types

        byte num1 = 100;

        System.out.println(num1);

        short num2 = 32767;
        System.out.println(num2);

        int num3 = 40000;
        System.out.println(num3);

        long num4 = 232321112;
        System.out.println(num4);

        float num5 = 3.14f;
        System.out.println(num5);

        double num6 = 3.166968164;
        System.out.println(num6);

        boolean eligibleToVote = true;
        System.out.println(eligibleToVote);

        char firstCharacter = 'R';
        System.out.println(firstCharacter);

        // typecasting => datatype implicit or explicit conversion

        long newNum = num1;
        System.out.println("new num: " + newNum);


    }
}
